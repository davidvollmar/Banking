	private void jButton_transferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_transferActionPerformed
		String amountStr = jTextField_amount.getText();
		String accountNumberStr = jTextField_accountnumber.getText();
		long amount = -1;
		int accountNumber = -1;
		long saldo = -1;
		long limit = -1;
		try {
			amount = Integer.parseInt(amountStr);
			accountNumber = Integer.parseInt(accountNumberStr);
			saldo = session.getSaldo();
			limit = session.getLimit();
		} catch (NumberFormatException e) {
			showError("Input Error", "Please enter a positive number.");
			Logger.getGlobal().log(Level.INFO, e.getLocalizedMessage(), e);
			return;
		} catch (RemoteException | NotAuthenticatedException ex) {
			Logger.getLogger(AccountView.class.getName()).log(Level.SEVERE, null, ex);
			showError("Connection error", "Connection error on processing your transfer.");
		}
		if (amount <= 0) {
			showError("Invalid amount", "Please enter a positive amount to transfer.");
			return;
		}
		if (amount > (saldo + -1*limit)) {
			showError("Not enough funds", "Not enough funds to process your transaction.");
		}
		if (accountNumber <= 0) {
			showError("Unknown account number", "The account number you've entered does not seem to exist.");
			return;
		}
		try {
			if (!session.transfer(accountNumber, amount)) {
				showError("Transaction failed", "Your transaction has not been processed. Please try again.");
			}

		} catch (RemoteException | NotAuthenticatedException ex) {
			Logger.getLogger(AccountView.class.getName()).log(Level.SEVERE, null, ex);
			showError("Connection error", "Connection error on processing your transfer.");
		}
		alert("Tranfer processed succesfully", "Your transfer of " + amount + " to " + accountNumber + "was processed succefully.");
		jTextField_accountnumber.setText("");
		jTextField_amount.setText("");
		initValues();
	}//GEN-LAST:event_jButton_transferActionPerformed

